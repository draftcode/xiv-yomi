def to_segments(s: str):
    longest_fseg = None
    for fseg in FORCED_SEGMENTS.keys():
        if fseg in s:
            if longest_fseg is None:
                longest_fseg = fseg
            elif len(longest_fseg) < len(fseg):
                longest_fseg = fseg
    if longest_fseg is not None:
        ss = s.split(longest_fseg, 2)
        ret = to_segments(ss[0])
        ret.append(longest_fseg)
        ret.extend(to_segments(ss[1]))
        return ret
    i = 0
    ret = []
    while i < len(s):
        cs = [s[i]]
        j = i + 1
        while j < len(s) and unicodeblock.blocks.of(
                s[i]) == unicodeblock.blocks.of(s[j]):
            cs.append(s[j])
            j += 1
        ret.append(''.join(cs))
        i = j
    return ret


def katakana_to_hiragana_simple(s: str):
    """Simplified katakana_to_hiragana

    wanakana's katakana_to_hiragana does an unnecessary conversion. This is a
    simplified one.
    """
    out = []
    for c in s:
        if is_char_katakana(c):
            out.append(katakana_to_hiragana(c, None, False))
        else:
            out.append(c)
    return ''.join(out)


def to_yomi(s: str):
    segments = to_segments(s)
    yomi = ''
    for seg in segments:
        if seg in FORCED_SEGMENTS:
            yomi += FORCED_SEGMENTS[seg]
            continue
        block = unicodeblock.blocks.of(seg[0])
        if block in [
                'CJK_SYMBOLS_AND_PUNCTUATION',
                'HALFWIDTH_AND_FULLWIDTH_FORMS',
        ]:
            yomi += seg
            continue
        if block in [
                'SPACE',
                'DIGIT',
                'HIRAGANA',
                'BASIC_LATIN',
                'BASIC_PUNCTUATION',
                'KATAKANA',
        ]:
            yomi += seg
            continue
        if seg in YOMI:
            yomi += YOMI[seg]
            continue
        raise ValueError("Unknown segment: " + seg)
    return katakana_to_hiragana_simple(yomi)
